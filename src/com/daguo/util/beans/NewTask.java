public class NewTask {
	private String name;
	private String id;
	private String grade;
	private String state;

	/**
	 * @return the name
	 */
	public String getName() {
	    return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
	    this.name = name;
	}

	/**
	 * @return the id
	 */
	public String getId() {
	    return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(String id) {
	    this.id = id;
	}

	/**
	 * @return the grade
	 */
	public String getGrade() {
	    return grade;
	}

	/**
	 * @param grade
	 *            the grade to set
	 */
	public void setGrade(String grade) {
	    this.grade = grade;
	}

	/**
	 * @return the state
	 */
	public String getState() {
	    return state;
	}

	/**
	 * @param state
	 *            the state to set
	 */
	public void setState(String state) {
	    this.state = state;
	}

}

